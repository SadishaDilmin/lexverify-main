// ── Rule functions ───────────────────────────────────────────────

function ruleUndeclaredIncoming(
  assessments: MaterialityAssessment[],
  txs: ClassifiedTransaction[]
): ExceptionItem[] {
  const results: ExceptionItem[] = [];
  for (const a of assessments) {
    const tx = a.classifiedTransaction;
    if (
      tx.direction === 'incoming' &&
      !tx.linked_fund_source_id &&
      tx.explanation_status !== 'mapped' &&
      tx.explanation_status !== 'not_relevant' &&
      (tx.amount ?? 0) >= 1000
    ) {
      results.push({
        exceptionType: 'undeclared_incoming_credit',
        severity: (tx.amount ?? 0) >= 10_000 ? 'critical' : 'high',
        rationale: `Incoming credit of £${tx.amount} on ${tx.tx_date ?? 'unknown'} ("${tx.description ?? ''}") not linked to any declared source.`,
        quantitativeBasis: `£${tx.amount}`,
        linkedRefTable: 'sow_transactions',
        linkedRefId: tx.id,
        linkedField: 'linked_fund_source_id',
        autoGenerated: true,
        reviewerConfirmationRequired: true,
        suggestedNextAction: 'ENQUIRY: Request explanation and evidence for this credit.',
        canTriggerEnquiry: true,
      });
    }
  }
  return results;
}

/** Categories that should NOT trigger unexplained_large_incoming when classified with high confidence */
const SAFE_LARGE_CATEGORIES = new Set([
  'salary_credit',
  'bonus_credit',
  'government_bonus',
  'sale_proceeds',
  'investment_withdrawal',
  'refund',
]);

function ruleUnexplainedLargeIncoming(
  assessments: MaterialityAssessment[],
  amountToProve?: number
): ExceptionItem[] {
  const results: ExceptionItem[] = [];
  for (const a of assessments) {
    const tx = a.classifiedTransaction;
    const amt = tx.amount ?? 0;
    const isLarge = amt >= 5000 || tx.is_large === true;

    if (!isLarge || tx.direction !== 'incoming' || tx.linked_fund_source_id) continue;

    // High-confidence safe categories (salary, bonus, refund, etc.) — skip
    const conf = typeof tx.classificationConfidence === 'number' ? tx.classificationConfidence : 0;
    const isSafe = SAFE_LARGE_CATEGORIES.has(tx.classifiedCategory) && conf >= 0.8;
    const isGiftHighConf = tx.classifiedCategory === 'gift' && conf >= 0.8;

    if (isSafe || isGiftHighConf) continue;

    // Severity: proportion-based when amountToProve is available
    let severity: 'critical' | 'high' | 'medium';
    if (amountToProve && amountToProve > 0 && amt / amountToProve > 0.2) {
      severity = 'critical';
    } else if (amt >= 5000) {
      severity = 'high';
    } else {
      severity = 'medium';
    }

    results.push({
      exceptionType: 'unexplained_large_incoming',
      severity,
      rationale: `Large incoming credit of £${amt} on ${tx.tx_date ?? 'unknown'} ("${tx.description ?? ''}") is unexplained or only weakly classified (category: ${tx.classifiedCategory}, confidence: ${(conf * 100).toFixed(0)}%).`,
      quantitativeBasis: `£${amt}`,
      linkedRefTable: 'sow_transactions',
      linkedRefId: tx.id,
      linkedField: null,
      autoGenerated: true,
      reviewerConfirmationRequired: true,
      suggestedNextAction: 'ENQUIRY: Identify source and request supporting evidence for this credit.',
      canTriggerEnquiry: true,
    });
  }
  return results;
}

const REPEATED_RISK_CATEGORIES = new Set([
  'third_party_transfer',
  'unknown_incoming',
  'own_account_transfer',
  'recurring_non_salary',
]);

function ruleRepeatedThirdParty(
  repeatedPatterns: RepeatedPattern[]
): ExceptionItem[] {
  const results: ExceptionItem[] = [];
  for (const p of repeatedPatterns) {
    if (REPEATED_RISK_CATEGORIES.has(p.category)) {
      results.push({
        exceptionType: 'repeated_third_party_credits',
        severity: p.totalAmount >= 10_000 ? 'critical' : 'high',
        rationale: `${p.count} repeated ${p.category} credits totalling £${p.totalAmount.toFixed(2)} without explanation.`,
        quantitativeBasis: `${p.count} transactions, £${p.totalAmount.toFixed(2)} total`,
        linkedRefTable: 'sow_transactions',
        linkedRefId: p.transactionIds[0] ?? '',
        linkedField: null,
        autoGenerated: true,
        reviewerConfirmationRequired: true,
        suggestedNextAction: 'ENQUIRY: Request identity and purpose of recurring third-party payments.',
        canTriggerEnquiry: true,
      });
    }
  }
  return results;
}

function ruleCashDeposit(txs: ClassifiedTransaction[]): ExceptionItem[] {
  const results: ExceptionItem[] = [];
  const cashTxs = txs.filter(
    (t) => t.direction === 'incoming' && t.classifiedCategory === 'cash_deposit' && (t.amount ?? 0) >= 500
  );
  for (const tx of cashTxs) {
    results.push({
      exceptionType: 'cash_deposit_unexplained',
      severity: (tx.amount ?? 0) >= 5000 ? 'critical' : 'high',
      rationale: `Cash deposit of £${tx.amount} on ${tx.tx_date ?? 'unknown'}. Cash deposits require enhanced scrutiny.`,
      quantitativeBasis: `£${tx.amount}`,
      linkedRefTable: 'sow_transactions',
      linkedRefId: tx.id,
      linkedField: null,
      autoGenerated: true,
      reviewerConfirmationRequired: true,
      suggestedNextAction: 'ENQUIRY: Request explanation and evidence of cash origin.',
      canTriggerEnquiry: true,
    });
  }
  return results;
}

function ruleReconciliationExceptions(
  reconciliations: SourceReconciliation[]
): ExceptionItem[] {
  const results: ExceptionItem[] = [];

  for (const r of reconciliations) {
    const cat = r.sourceCategory.toLowerCase();

    // Amount mismatch
    if (r.unsupportedAmount > 0 && r.declaredAmount > 0 && r.unsupportedAmount / r.declaredAmount > 0.1) {
      results.push({
        exceptionType: 'amount_mismatch_declaration_vs_evidence',
        severity: r.unsupportedAmount / r.declaredAmount > 0.5 ? 'critical' : 'high',
        rationale: `Source "${r.sourceCategory}": £${r.unsupportedAmount.toFixed(2)} unsupported of £${r.declaredAmount} declared.`,
        quantitativeBasis: `Supported: £${r.supportedAmount.toFixed(2)}, Unsupported: £${r.unsupportedAmount.toFixed(2)}`,
        linkedRefTable: 'sow_fund_sources',
        linkedRefId: r.fundSourceId,
        linkedField: 'declared_amount',
        autoGenerated: true,
        reviewerConfirmationRequired: true,
        suggestedNextAction: 'ENQUIRY: Request further evidence or explanation for the unsupported amount.',
        canTriggerEnquiry: true,
      });
    }

    // Timing mismatch
    if (r.timingMismatch) {
      results.push({
        exceptionType: 'source_timing_mismatch',
        severity: 'medium',
        rationale: `Source "${r.sourceCategory}" declared receipt date does not align with linked transaction dates.`,
        quantitativeBasis: null,
        linkedRefTable: 'sow_fund_sources',
        linkedRefId: r.fundSourceId,
        linkedField: 'date_received',
        autoGenerated: true,
        reviewerConfirmationRequired: true,
        suggestedNextAction: 'ENQUIRY: Clarify the date funds were received.',
        canTriggerEnquiry: true,
      });
    }

    // Circular movement
    if (r.circularMovementDetected) {
      results.push({
        exceptionType: 'circular_movement_suspected',
        severity: 'critical',
        rationale: `Source "${r.sourceCategory}": transactions suggest possible circular movement of funds between accounts.`,
        quantitativeBasis: `Declared: £${r.declaredAmount}`,
        linkedRefTable: 'sow_fund_sources',
        linkedRefId: r.fundSourceId,
        linkedField: null,
        autoGenerated: true,
        reviewerConfirmationRequired: true,
        suggestedNextAction: 'ENQUIRY: Request full audit trail showing origin and destination of funds.',
        canTriggerEnquiry: true,
      });
    }

    // Salary savings unsupported
    if (cat.includes('salary') && r.reconciliationStatus !== 'fully_reconciled' && r.mismatchReasons.some((m) => m.includes('accumulation'))) {
      results.push({
        exceptionType: 'salary_savings_unsupported',
        severity: 'high',
        rationale: `Salary savings claim of £${r.declaredAmount} not adequately supported by income pattern.`,
        quantitativeBasis: `Declared: £${r.declaredAmount}, Supported: £${r.supportedAmount.toFixed(2)}`,
        linkedRefTable: 'sow_fund_sources',
        linkedRefId: r.fundSourceId,
        linkedField: 'declared_amount',
        autoGenerated: true,
        reviewerConfirmationRequired: true,
        suggestedNextAction: 'ENQUIRY: Request savings history and detailed explanation of accumulation.',
        canTriggerEnquiry: true,
      });
    }

    // Co-buyer unevidenced — fires when unreconciled, pending review, OR no independent evidence
    if (
      (cat.includes('co-buyer') || cat.includes('co_buyer')) &&
      (r.reconciliationStatus === 'unreconciled' ||
       r.reconciliationStatus === 'pending_review' ||
       r.linkedEvidenceCount === 0)
    ) {
      const noEvidence = r.linkedEvidenceCount === 0;
      results.push({
        exceptionType: 'cobuyer_contribution_unevidenced',
        severity: 'high',
        rationale: noEvidence && r.reconciliationStatus !== 'unreconciled'
          ? `Co-buyer contribution of £${r.declaredAmount} has an inferred transaction match but no independent evidence (no linked evidence items).`
          : `Co-buyer contribution of £${r.declaredAmount} has no supporting evidence.`,
        quantitativeBasis: `£${r.declaredAmount}`,
        linkedRefTable: 'sow_fund_sources',
        linkedRefId: r.fundSourceId,
        linkedField: 'declared_amount',
        autoGenerated: true,
        reviewerConfirmationRequired: true,
        suggestedNextAction: 'ENQUIRY: Request bank statements and source evidence from co-buyer.',
        canTriggerEnquiry: true,
      });
    }

    // Gift incomplete
    if (cat.includes('gift') && (r.reconciliationStatus === 'unreconciled' || r.linkedEvidenceCount === 0)) {
      results.push({
        exceptionType: 'gift_incomplete_evidence',
        severity: 'high',
        rationale: `Gift of £${r.declaredAmount} lacks complete evidence (donor letter, bank statement, or relationship proof).`,
        quantitativeBasis: `£${r.declaredAmount}`,
        linkedRefTable: 'sow_fund_sources',
        linkedRefId: r.fundSourceId,
        linkedField: null,
        autoGenerated: true,
        reviewerConfirmationRequired: true,
        suggestedNextAction: 'ENQUIRY: Request gift letter, donor bank statement, and proof of donor relationship.',
        canTriggerEnquiry: true,
      });
    }
  }

  return results;
}

function ruleFundingChainExceptions(chain: FundingChainSummary): ExceptionItem[] {
  const results: ExceptionItem[] = [];

  if (chain.hasShortfall) {
    const shortfallProportion = chain.amountToProve > 0
      ? chain.shortfallAmount / chain.amountToProve : 1;

    // Cross-check: if report header says no shortfall, downgrade
    const reportSaysNoShortfall = chain.reportHeaderExcessShortfall != null
      && chain.reportHeaderExcessShortfall >= 0;

    // Materiality gate: trivial shortfalls get suppressed or downgraded
    const isTrivial = shortfallProportion < 0.05 && chain.shortfallAmount < 2000;

    if (!(reportSaysNoShortfall && isTrivial)) {
      // Determine severity with nuance
      let severity: 'critical' | 'high' | 'medium' | 'low';
      if (reportSaysNoShortfall || isTrivial) {
        severity = 'low';
      } else if (shortfallProportion > 0.2) {
        severity = 'critical';
      } else {
        severity = 'high';
      }

      results.push({
        exceptionType: 'funding_shortfall',
        severity,
        rationale: `Funding shortfall of £${chain.shortfallAmount.toFixed(2)}. Amount to prove: £${chain.amountToProve}, evidenced: £${chain.totalEvidencedFunds.toFixed(2)}.${reportSaysNoShortfall ? ' Note: Armalytix report header indicates no shortfall — gap may reflect limited transaction visibility.' : ''}`,
        quantitativeBasis: `Shortfall: £${chain.shortfallAmount.toFixed(2)} (${(shortfallProportion * 100).toFixed(1)}% of amount to prove)`,
        linkedRefTable: 'armalytix_reports',
        linkedRefId: '',
        linkedField: 'amount_to_prove',
        autoGenerated: true,
        reviewerConfirmationRequired: true,
        suggestedNextAction: 'ENQUIRY: Request explanation and evidence for funding gap.',
        canTriggerEnquiry: true,
      });
    }
  }

  if (chain.hasExcess && !chain.excessExplained) {
    results.push({
      exceptionType: 'excess_funds_unexplained',
      severity: 'medium',
      rationale: `Excess funds of £${chain.excessAmount.toFixed(2)} above amount to prove. The excess is not fully explained.`,
      quantitativeBasis: `Excess: £${chain.excessAmount.toFixed(2)}`,
      linkedRefTable: 'armalytix_reports',
      linkedRefId: '',
      linkedField: 'excess_shortfall',
      autoGenerated: true,
      reviewerConfirmationRequired: false,
      suggestedNextAction: 'NOTE: Consider whether excess requires further investigation.',
      canTriggerEnquiry: false,
    });
  }

  if (chain.unsupportedManualBalances > 0) {
    results.push({
      exceptionType: 'manual_balance_unevidenced',
      severity: 'high',
      rationale: `Manual balances of £${chain.unsupportedManualBalances.toFixed(2)} relied upon for proof of funds but lacking evidence.`,
      quantitativeBasis: `£${chain.unsupportedManualBalances.toFixed(2)}`,
      linkedRefTable: 'sow_manual_balances',
      linkedRefId: '',
      linkedField: 'counted_toward_proof',
      autoGenerated: true,
      reviewerConfirmationRequired: true,
      suggestedNextAction: 'ENQUIRY: Request verified bank statement or screenshot for manual balance.',
      canTriggerEnquiry: true,
    });
  }

  if (chain.giftsUnproven > 0) {
    results.push({
      exceptionType: 'gift_incomplete_evidence',
      severity: 'high',
      rationale: `Gift funds of £${chain.giftsUnproven.toFixed(2)} declared but not adequately evidenced.`,
      quantitativeBasis: `£${chain.giftsUnproven.toFixed(2)}`,
      linkedRefTable: 'sow_fund_sources',
      linkedRefId: '',
      linkedField: null,
      autoGenerated: true,
      reviewerConfirmationRequired: true,
      suggestedNextAction: 'ENQUIRY: Request gift letter, donor bank statement, and proof of donor SoW.',
      canTriggerEnquiry: true,
    });
  }

  if (chain.coBuyerUnproven > 0) {
    results.push({
      exceptionType: 'cobuyer_contribution_unevidenced',
      severity: 'high',
      rationale: `Co-buyer contributions of £${chain.coBuyerUnproven.toFixed(2)} declared but not evidenced.`,
      quantitativeBasis: `£${chain.coBuyerUnproven.toFixed(2)}`,
      linkedRefTable: 'case_parties',
      linkedRefId: '',
      linkedField: 'contribution_amount',
      autoGenerated: true,
      reviewerConfirmationRequired: true,
      suggestedNextAction: 'ENQUIRY: Request co-buyer bank statements and source of funds evidence.',
      canTriggerEnquiry: true,
    });
  }

  return results;
}

function ruleActivityExceptions(txs: ClassifiedTransaction[]): ExceptionItem[] {
  const results: ExceptionItem[] = [];

  // Gambling
  const gamblingTxs = txs.filter((t) => t.classifiedCategory === 'gambling');
  const totalGambling = gamblingTxs.reduce((s, t) => s + (t.amount ?? 0), 0);
  if (totalGambling >= 500) {
    results.push({
      exceptionType: 'significant_gambling_activity',
      severity: totalGambling >= 5000 ? 'critical' : 'high',
      rationale: `${gamblingTxs.length} gambling transactions totalling £${totalGambling.toFixed(2)} detected.`,
      quantitativeBasis: `${gamblingTxs.length} transactions, £${totalGambling.toFixed(2)}`,
      linkedRefTable: 'sow_transactions',
      linkedRefId: gamblingTxs[0]?.id ?? '',
      linkedField: null,
      autoGenerated: true,
      reviewerConfirmationRequired: true,
      suggestedNextAction: 'ENQUIRY: Assess impact on relied-upon funds; request explanation if funds are being relied upon.',
      canTriggerEnquiry: true,
    });
  }

  // Crypto / investment
  const cryptoTxs = txs.filter((t) => t.classifiedCategory === 'crypto' || t.classifiedCategory === 'investment');
  const totalCrypto = cryptoTxs.reduce((s, t) => s + (t.amount ?? 0), 0);
  if (totalCrypto >= 500) {
    results.push({
      exceptionType: 'investment_crypto_activity',
      severity: totalCrypto >= 10_000 ? 'high' : 'medium',
      rationale: `${cryptoTxs.length} investment/crypto transactions totalling £${totalCrypto.toFixed(2)} detected.`,
      quantitativeBasis: `${cryptoTxs.length} transactions, £${totalCrypto.toFixed(2)}`,
      linkedRefTable: 'sow_transactions',
      linkedRefId: cryptoTxs[0]?.id ?? '',
      linkedField: null,
      autoGenerated: true,
      reviewerConfirmationRequired: true,
      suggestedNextAction: 'ENQUIRY: Determine if investment proceeds are relied upon for purchase.',
      canTriggerEnquiry: true,
    });
  }

  // Possible undeclared loan
  const loanTxs = txs.filter(
    (t) => t.direction === 'incoming' && t.classifiedCategory === 'loan_receipt' && !t.linked_fund_source_id
  );
  for (const tx of loanTxs) {
    results.push({
      exceptionType: 'possible_undeclared_loan',
      severity: 'high',
      rationale: `Incoming credit of £${tx.amount} on ${tx.tx_date ?? 'unknown'} appears loan-related but no loan declared in sources.`,
      quantitativeBasis: `£${tx.amount}`,
      linkedRefTable: 'sow_transactions',
      linkedRefId: tx.id,
      linkedField: null,
      autoGenerated: true,
      reviewerConfirmationRequired: true,
      suggestedNextAction: 'ENQUIRY: Clarify whether this is a loan and request loan agreement if applicable.',
      canTriggerEnquiry: true,
    });
  }

  // Overseas unexplained
  const overseasTxs = txs.filter(
    (t) => t.direction === 'incoming' && t.classifiedCategory === 'overseas_incoming' && !t.linked_fund_source_id
  );
  for (const tx of overseasTxs) {
    results.push({
      exceptionType: 'overseas_insufficiently_explained',
      severity: 'high',
      rationale: `Overseas incoming credit of £${tx.amount} on ${tx.tx_date ?? 'unknown'} not linked to a declared source.`,
      quantitativeBasis: `£${tx.amount}`,
      linkedRefTable: 'sow_transactions',
      linkedRefId: tx.id,
      linkedField: null,
      autoGenerated: true,
      reviewerConfirmationRequired: true,
      suggestedNextAction: 'ENQUIRY: Request originating country, institution, currency details, and purpose.',
      canTriggerEnquiry: true,
    });
  }

  return results;
}

function ruleContradictionBridge(contradictions: ContradictionResult[]): ExceptionItem[] {
  const results: ExceptionItem[] = [];

  for (const c of contradictions) {
    // Map existing contradiction types to exception types where applicable
    let exType: ExceptionType | null = null;
    if (c.type === 'mortgage_contradiction') exType = 'mortgage_funding_contradiction';
    else if (c.type === 'name_mismatch') exType = 'name_identity_inconsistency';
    else continue; // Already covered by other rules

    results.push({
      exceptionType: exType,
      severity: c.severity,
      rationale: c.summary,
      quantitativeBasis: null,
      linkedRefTable: c.affectedRefTable,
      linkedRefId: c.affectedRefId,
      linkedField: c.affectedField ?? null,
      autoGenerated: c.autoGenerated,
      reviewerConfirmationRequired: true,
      suggestedNextAction: 'ENQUIRY: Investigate and resolve the identified contradiction.',
      canTriggerEnquiry: true,
    });
  }

  return results;
}

// ── Deduplication ────────────────────────────────────────────────

function deduplicateExceptions(exceptions: ExceptionItem[]): ExceptionItem[] {
  const seen = new Set<string>();
  return exceptions.filter((e) => {
    const key = `${e.exceptionType}::${e.linkedRefTable}::${e.linkedRefId}::${e.linkedField ?? ''}`;
    if (seen.has(key)) return false;
    seen.add(key);
    return true;
  });
}

